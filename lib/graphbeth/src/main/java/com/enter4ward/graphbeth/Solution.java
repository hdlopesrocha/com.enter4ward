package com.enter4ward.graphbeth;

public class Solution  {
	
	private float min;
	private float max;
	
	
	public Solution(float min, float max) {
		super();
		this.min = min;
		this.max = max;
	}

	public float getMin() {
		return min;
	}
	public float getMax() {
		return max;
	}

	public void setMax(float x) {
		this.max = x;
	}
	
	public void setMin(float x) {
		this.min = x;
	}
	
	

	
}
